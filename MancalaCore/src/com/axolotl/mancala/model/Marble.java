package com.axolotl.mancala.model;

/**
 * Represents a marble in the system
 */
public class Marble {
	
	private final MarbleColour colour;
	
	/**
	 * Constructs a new instance of the Marble class
	 * @param colour
	 */
	public Marble(MarbleColour colour) {
		this.colour = colour;
	}
	
	/**
	 * Gets the colour of this marble
	 * 
	 * @return The marble colour
	 */
	public MarbleColour getColour() {
		return colour;
	}
}
